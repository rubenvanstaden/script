# Description and purpose of script.

set -e

colors() {

    # If this doesn't work, enter an ESC directly.
	esc="\033"

	# Foreground colors
	red="${esc}[31m"
	green="${esc}[32m"
	yellow="${esc}[33m"
    blue="${esc}[34m"
	purple="${esc}[35m"
	cyan="${esc}[36m"
	white="${esc}[37m"

	# Bold, italic, underline, and inverse style toggles
	boldon="${esc}[1m"
	boldoff="${esc}[22m"
	italicson="${esc}[3m"
	italicsoff="${esc}[23m"
	reset="${esc}[0m"
}
